// Get saved language or default to Indonesian
let currentLanguage = localStorage.getItem("language") || "id";

// Function to change language
function changeLanguage(lang) {
  // Check if Arabic is clicked - show coming soon message
  if (lang === "ar") {
    alert(
      "Ù‚Ø±ÙŠØ¨Ø§Ù‹ - Coming Soon\n\nThe Arabic version is currently under development."
    );
    return;
  }

  currentLanguage = lang;
  localStorage.setItem("language", lang);

  // Update all elements with data-translate attribute
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate");
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  // Update typed.js text
  updateTypedText();

  // Update active language button
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.remove("active");
  });
  document.querySelector(`[data-lang="${lang}"]`).classList.add("active");

  // Update HTML dir attribute for RTL support
  if (lang === "ar") {
    document.documentElement.setAttribute("dir", "rtl");
    document.body.classList.add("rtl");
  } else {
    document.documentElement.setAttribute("dir", "ltr");
    document.body.classList.remove("rtl");
  }
}

// Function to update Typed.js
function updateTypedText() {
  const typingElement = document.querySelector(".typing-text");
  if (typingElement && typeof Typed !== "undefined") {
    // Destroy existing typed instance if it exists
    if (window.typedInstance) {
      window.typedInstance.destroy();
    }

    // Create new typed instance with current language
    window.typedInstance = new Typed(".typing-text", {
      strings: [
        translations[currentLanguage]["home.typing1"],
        translations[currentLanguage]["home.typing2"],
        translations[currentLanguage]["home.typing3"],
      ],
      loop: true,
      typeSpeed: 55,
      backSpeed: 25,
      backDelay: 500,
    });
  }
}

// Initialize language on page load
document.addEventListener("DOMContentLoaded", function () {
  changeLanguage(currentLanguage);

  // Add event listeners to language buttons
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", function () {
      const lang = this.getAttribute("data-lang");
      changeLanguage(lang);
    });
  });
});
